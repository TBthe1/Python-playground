# Iteration

# break --> verlaat binnenste lus
# continue --> keert terug naar begin van lus
# pass --> gebruik als tijdelijke TODO

# For lus
# Start default 0
# Step default 1
# bv. for index in range(start, stop, step):
#       something

startValue = 1

for i in range(5):
    startValue += 1

print(startValue)

# While lus
# Bv. while condition:
#       something

secondStartValue = 1

while secondStartValue <= 10:
    secondStartValue += 1

print(secondStartValue)
