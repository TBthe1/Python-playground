# Rows and lists

# Ongeorde verzameling items + veranderbaar

# Element ophalen o.b.v. index
# Laatste element staat op -1
fruits = ['orange', 'apple', 'pear', 'banana', 'kiwi', 'apple', 'banana']
print(fruits[0])
print(fruits[-3])

# Sorteer een list
sortedFruits = sorted(fruits)
print(sortedFruits)

